package org.example.chapter3.exercises;

public class Exercise3 {
    public static void main(String[] args) {
        int x = 1;
        System.out.println(x++);

        x = 1;
        System.out.println(++x);

        x = 1;
        x = x + 1;
        System.out.println(x);

        x = 1;
        x += 1;
        System.out.println(x);
    }
}
